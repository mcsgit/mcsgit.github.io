<p>În dinamica societății actuale a apărut necesitatea unor entități private pentru efectuarea investigațiilor și consultanță în domeniu, respectiv societățile specializate și cabinetele individuale de  <b>detectivi particulari</b>. Activitatea acestora este reglementată în principal de Legea nr. 329 din 2003. Condiția principală impusă detectivilor particulari este ca aceștia să nu aducă atingere drepturilor și libertăților fundamentale ale omului în decursul efectuării investigațiilor.</p>
